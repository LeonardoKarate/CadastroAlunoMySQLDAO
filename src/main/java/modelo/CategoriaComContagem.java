package modelo;

/**
 *
 * @author leonardoNunes
 */
public class CategoriaComContagem {
    private String nome;
    private int total;
    
    public CategoriaComContagem(String nome, int total) {
        this.nome = nome;
        this.total = total;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the total
     */
    public int getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(int total) {
        this.total = total;
    }
    
}
